package com.bridgelabz.pp1;

public class DefaultValues {
    static byte a1;
    static short a2;
    static int a3;
    static long a4;
    static float a5;
    static double a6;
    static char a7;
    static boolean a8;
    public static void main(String[] args) {
        System.out.println("Default value of Byte is " + a1);
        System.out.println("Default value of Short is " + a2);
        System.out.println("Default value of Int is " + a3);
        System.out.println("Default value of Long is " + a4);
        System.out.println("Default value of Float is " + a5 + "f");
        System.out.println("Default value of Double is " + a6 + "d");
        System.out.println("Default value of character is " + a7);
        System.out.println("Default value of Boolean is " + a8);
    }
}
